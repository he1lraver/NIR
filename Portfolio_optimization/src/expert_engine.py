"""
Модуль экспертного анализа портфеля.

Генерирует текстовые отчёты на основе рассчитанных метрик,
давая интерпретацию результатов в стиле профессионального аналитика.

Author: [Ваше ФИО]
Version: 1.0
"""
import numpy as np


class ExpertAnalyst:
    """
    Класс для генерации экспертных заключений по портфелю.
    
    Анализирует:
        - Эффективность по Шарпу
        - Хвостовые риски (CVaR, Max Drawdown)
        - Уровень диверсификации (корреляции)
        - Концентрацию позиций
    """
    
    @staticmethod
    def generate_report(metrics: dict, returns_df, weights) -> str:
        """
        Генерация текстового отчёта с рекомендациями.
        
        Args:
            metrics: Словарь с метриками портфеля
            returns_df: DataFrame с доходностями активов
            weights: Вектор весов портфеля
            
        Returns:
            Форматированный текст отчёта
        """
        report = []
        
        # Оценка эффективности по коэффициенту Шарпа
        sharpe = metrics['sharpe']
        if sharpe > 1.5:
            report.append(
                "**Оценка эффективности:** Портфель демонстрирует **выдающуюся** "
                "эффективность по Шарпу (>1.5). Риск щедро компенсируется доходностью."
            )
        elif sharpe > 0.8:
            report.append(
                "**Оценка эффективности:** Портфель имеет **хороший** профиль риск/доходность. "
                "Это крепкий рыночный портфель."
            )
        else:
            report.append(
                "**Оценка эффективности:** Эффективность **низкая**. Портфель берёт на себя "
                "слишком много риска ради текущей доходности. Рекомендуется пересмотр активов."
            )

        # Анализ хвостовых рисков
        cvar = metrics['cvar_95']
        max_dd = metrics['max_drawdown']
        report.append(
            f"**Хвостовые риски (Tail Risks):** Максимальная историческая просадка составила "
            f"**{max_dd*100:.1f}%**. Метрика условного VaR (CVaR) указывает, что в худшие 5% "
            f"сценариев ожидаемый убыток составит **{cvar*100:.1f}%** годовых. Убедитесь, что ваш "
            f"риск-профиль допускает такие просадки."
        )

        # Оценка диверсификации через среднюю корреляцию
        corr_matrix = returns_df.corr()
        n = len(corr_matrix)
        # Извлекаем элементы выше главной диагонали
        upper_tri_indices = np.triu_indices_from(corr_matrix.values, k=1)
        avg_corr = corr_matrix.values[upper_tri_indices].mean()
        
        if avg_corr > 0.7:
            report.append(
                "**Диверсификация:** Активы **сильно коррелированы** (в среднем >0.7). При падении "
                "рынка портфель будет падать целиком. Рассмотрите добавление активов из других "
                "секторов (золото, облигации, IT)."
            )
        elif avg_corr < 0.3:
            report.append(
                "**Диверсификация:** Портфель **отлично диверсифицирован**. Активы слабо зависят "
                "друг от друга, что снижает общий риск."
            )
        else:
            report.append(
                f"**Диверсификация:** Уровень корреляции **средний** ({avg_corr:.2f}). Защита от "
                "системного риска присутствует, но может быть улучшена."
            )
            
        # Анализ концентрации портфеля
        tickers = returns_df.columns
        dominant_idx = np.argmax(weights)
        if weights[dominant_idx] > 0.4:
            report.append(
                f"**Концентрация:** Замечен сильный перекос в актив **{tickers[dominant_idx]}** "
                f"({weights[dominant_idx]*100:.1f}%). Это создаёт недиверсифицируемый "
                "(идиосинкразический) риск одного эмитента."
            )

        return "\n\n".join(report)