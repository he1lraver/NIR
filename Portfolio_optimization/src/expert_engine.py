import numpy as np

class ExpertAnalyst:
    @staticmethod
    def generate_report(metrics: dict, returns_df, weights) -> str:
        report = []
        
        # 1. Доходность и риск
        sharpe = metrics['sharpe']
        if sharpe > 1.5:
            report.append("**Оценка эффективности:** Портфель демонстрирует **выдающуюся** эффективность по Шарпу (>1.5). Риск щедро компенсируется доходностью.")
        elif sharpe > 0.8:
            report.append("**Оценка эффективности:** Портфель имеет **хороший** профиль риск/доходность. Это крепкий рыночный портфель.")
        else:
            report.append("**Оценка эффективности:** Эффективность **низкая**. Портфель берет на себя слишком много риска ради текущей доходности. Рекомендуется пересмотр активов.")

        # 2. Риск экстремальных потерь
        cvar = metrics['cvar_95']
        max_dd = metrics['max_drawdown']
        report.append(f"**Хвостовые риски (Tail Risks):** Максимальная историческая просадка составила **{max_dd*100:.1f}%**. Метрика условного VaR (CVaR) указывает, что в худшие 5% сценариев ожидаемый убыток составит **{cvar*100:.1f}%** годовых. Убедитесь, что ваш риск-профиль допускает такие просадки.")

        # 3. Диверсификация
        corr_matrix = returns_df.corr()
        avg_corr = corr_matrix.values[np.triu_indices_from(corr_matrix.values, k=1)].mean()
        
        if avg_corr > 0.7:
            report.append("**Диверсификация:** Активы **сильно коррелированы** (в среднем >0.7). При падении рынка портфель будет падать целиком. Рассмотрите добавление активов из других секторов (золото, облигации, IT).")
        elif avg_corr < 0.3:
            report.append("**Диверсификация:** Портфель **отлично диверсифицирован**. Активы слабо зависят друг от друга, что снижает общий риск (эффект бесплатного сыра по Марковицу).")
        else:
            report.append("**Диверсификация:** Уровень корреляции **средний**. Защита от системного риска присутствует, но может быть улучшена.")
            
        # 4. Рекомендации по весам
        tickers = returns_df.columns
        dominant_asset_idx = np.argmax(weights)
        if weights[dominant_asset_idx] > 0.4:
            report.append(f"**Концентрация:** Замечен сильный перекос в актив **{tickers[dominant_asset_idx]}** ({weights[dominant_asset_idx]*100:.1f}%). Это создает недиверсифицируемый (идиосинкразический) риск одного эмитента.")

        return "\n\n".join(report)